# Numbers are immutable datatypes in python
# There are three types of number datatype
# Integer, float and complex numbers


# Integer
a = 0
print(a)
a = 5
print(a)
a= -10
print(a)

# float
a = 0.0
b = 2.1
c = -5.4
d = 2e2  # float, 2 * 10 ** 2
print(d) # 200.0
e = 2e-2
print(e)  # 0.002

# Complex Numbers
a = 2 + 3j
print(type(a))
b = 2j
print(b)

c = -1j
print(c)

# Operations in Numbers
a = 2 + 3
print(a)  # 5 which is integer

b = 2 + 3.0
print(b)  # 5.0




